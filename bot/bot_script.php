function handleUpdate($update): void
{
    global $bot_name;

    logs(print_r($update, JSON_UNESCAPED_UNICODE),);

    if (!isset($update['message']['chat']['id'])) { // –ï—Å–ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–µ —Å–æ–¥–µ—Ä–∂–∏—Ç ID —á–∞—Ç–∞
