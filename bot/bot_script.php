        }

        $gameState['current_emoji'] = $unsolved_riddles[array_rand($unsolved_riddles)];
        $gameState['guessed_words'] = []; // –°–±—Ä–æ—Å —É–≥–∞–¥–∞–Ω–Ω—ã—Ö —Å–ª–æ–≤ –¥–ª—è –Ω–æ–≤–æ–π –∑–∞–≥–∞–¥–∫–∏
        $response_text .= PHP_EOL . "–°–ª–µ–¥—É—é—â–∞—è –∑–∞–≥–∞–¥–∫–∞: " . $gameState['current_emoji'];
