        $unsolved_riddles = array_diff(array_keys($riddles), $gameState['solved_riddles']);

        // –ï—Å–ª–∏ –≤—Å–µ –∑–∞–≥–∞–¥–∫–∏ —Ä–µ—à–µ–Ω—ã, –∑–∞–∫–∞–Ω—á–∏–≤–∞–µ–º –∏–≥—Ä—É
        if (empty($unsolved_riddles)) {
